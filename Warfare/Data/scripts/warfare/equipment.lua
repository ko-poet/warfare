Warfare.EquipBags = {}

function Warfare:getEquipBag(faction, slot, arg)
    local key = tostring(faction) .. ":" .. tostring(slot) .. ":" .. tostring(arg)

    self.EquipBags[key] = self.EquipBags[key] or {}

    return self.EquipBags[key], key
end

function Warfare:equipEntity(ent, visor)
    local clothing, weapon
    local soldierType = self.getType(ent)
    local faction = self.getFaction(ent)

    ent.inventory:RemoveAllItems()

    if soldierType == "infantry" then
        local cBag, cKey = self:getEquipBag(faction, "clothing", "heavy")
        clothing, self.EquipBags[cKey] = self.getRandomTableElement(self.Factions[faction].ClothingTable.Heavy, cBag)

        local wBag, wKey = self:getEquipBag(faction, "weapons", soldierType)
        weapon, self.EquipBags[wKey] = self.getRandomTableElement(self.Factions[faction].WeaponsTable.Infantry, wBag)

    elseif soldierType == "spearmen" then
        local cBag, cKey = self:getEquipBag(faction, "clothing", "heavy")
        clothing, self.EquipBags[cKey] = self.getRandomTableElement(self.Factions[faction].ClothingTable.Heavy, cBag)

        local wBag, wKey = self:getEquipBag(faction, "weapons", soldierType)
        weapon, self.EquipBags[wKey] = self.getRandomTableElement(self.Weapons.General.Polearm, wBag)

    elseif soldierType == "archers"then
        local cBag, cKey = self:getEquipBag(faction, "clothing", "light")
        clothing, self.EquipBags[cKey] = self.getRandomTableElement(self.Factions[faction].ClothingTable.Light, cBag)

        local wBag, wKey = self:getEquipBag(faction, "weapons", soldierType)
        weapon, self.EquipBags[wKey] = self.getRandomTableElement(self.Weapons.General.Bow, wBag)

    elseif soldierType == "crossbowmen" then
        local cBag, cKey = self:getEquipBag(faction, "clothing", "light")
        clothing, self.EquipBags[cKey] = self.getRandomTableElement(self.Factions[faction].ClothingTable.Light, cBag)

        local wBag, wKey = self:getEquipBag(faction, "weapons", soldierType)
        weapon, self.EquipBags[wKey] = self.getRandomTableElement(self.Weapons.General.Crossbow, wBag)

    elseif soldierType == "gunners" then
        local cBag, cKey = self:getEquipBag(faction, "clothing", "light")
        clothing, self.EquipBags[cKey] = self.getRandomTableElement(self.Factions[faction].ClothingTable.Light, cBag)

        local wBag, wKey = self:getEquipBag(faction, "weapons", soldierType)
        weapon, self.EquipBags[wKey] = self.getRandomTableElement(self.Weapons.General.Gun, wBag)

    elseif soldierType == "peasants" then
        local cBag, cKey = self:getEquipBag(faction, "clothing", "peasant")
        clothing, self.EquipBags[cKey] = self.getRandomTableElement(self.Clothing.Peasant, cBag)

        local wBag, wKey = self:getEquipBag(faction, "weapons", soldierType)
        weapon, self.EquipBags[wKey] = self.getRandomTableElement(self.Weapons.General.Pitchfork, wBag)
    end

    ent.actor:EquipClothingPreset(clothing)
    ent.actor:EquipWeaponPreset(weapon)
    --ent.human:ToggleWeaponSet(0)

    if soldierType == "spearmen" or soldierType == "peasants"then
        ent.human:ToggleWeaponSet(2)
    else
        ent.human:ToggleWeaponSet(1)
    end

    ent.human:DrawWeapon()

    if visor then
        self.setVisor(ent, true)
    end
end


