function Player:OnAction(action, activation, value)

    --Warfare.log(action .. " " .. activation .. " " .. value)

    if action == "warfare_preview_keyboard" then
        Warfare.Settings.Preview.active = not Warfare.Settings.Preview.active
        --Game.SendInfoText("Toggle Preview", false, nil, 2)
        if Warfare.Settings.Preview.active then
            Warfare.fight = false
        end
        Warfare:preview()

    elseif action == "warfare_spawn_keyboard" then
        if Warfare.Settings.Preview.active then
            Warfare:spawnUnit(Warfare.Settings.soldierType, Warfare.Settings.faction, Warfare.Settings.numSpawns, Warfare.Settings.rowSize)
        end

    elseif action == "warfare_despawn_keyboard" then
        Warfare:despawnAll(Warfare.SpawnedEntities)

    elseif action == "warfare_changeType_keyboard" then
        if Warfare.Settings.Preview.active then
            local index = Warfare.indexOf(Warfare.soldierTypes, Warfare.Settings.soldierType) + 1
            if index > #Warfare.soldierTypes then
                index = 1
            end
            Warfare.Settings.soldierType = Warfare.soldierTypes[index]
            Warfare.Settings.Preview.reload = true
            Warfare.log("Soldier Type: " .. Warfare.Settings.soldierType)
        end

    elseif action == "warfare_changeCoat_keyboard" then
        if Warfare.Settings.Preview.active then
            local index = Warfare.indexOf(Warfare.Coats, Warfare.Factions[Warfare.Settings.faction].coat) + 1
            if index > #Warfare.Coats then
                index = 1
            end
            Warfare.Factions[Warfare.Settings.faction].coat = Warfare.Coats[index]
            Warfare:setFactionTables(Warfare.Factions[Warfare.Settings.faction])
            Warfare.Settings.Preview.reload = true
            Warfare.log("Changed Faction " .. Warfare.Settings.faction .. " Coat to " .. Warfare.Factions[Warfare.Settings.faction].coat)
        end

    elseif action == "warfare_changeFaction_keyboard" then
        if Warfare.Settings.Preview.active then
            if Warfare.Settings.faction == 1 then
                Warfare.Settings.faction = 2
            else 
                Warfare.Settings.faction = 1
            end
            Warfare.Settings.Preview.reload = true
        end
    elseif action == "warfare_num_add_keyboard" then
        if Warfare.Keys.shift then
            Warfare.Settings.rowSize = Warfare.Settings.rowSize + 1
            Warfare.log("Row Size: " .. Warfare.Settings.rowSize)

        elseif Warfare.Keys.control then
            if Warfare.Settings.entSpace < 5 then
                Warfare.Settings.entSpace = Warfare.Settings.entSpace + 0.1
                Warfare.log("Space between Soldiers: " .. Warfare.Settings.entSpace)
            end

        else
            Warfare.Settings.numSpawns = Warfare.Settings.numSpawns + 1
            Warfare.log("Number of Spawns: " .. Warfare.Settings.numSpawns)
        end

    elseif action == "warfare_num_sub_keyboard" then
        if Warfare.Keys.shift then
            if Warfare.Settings.rowSize > 1 then
                Warfare.Settings.rowSize = Warfare.Settings.rowSize - 1
                Warfare.log("Row Size: " .. Warfare.Settings.rowSize)
            end

        elseif Warfare.Keys.control then
            if Warfare.Settings.entSpace > 0.7 then
                Warfare.Settings.entSpace = Warfare.Settings.entSpace - 0.1
                Warfare.log("Space between Soldiers: " .. Warfare.Settings.entSpace)
            end

        else
            if Warfare.Settings.numSpawns > 1 then
                Warfare.Settings.numSpawns = Warfare.Settings.numSpawns - 1
                Warfare.log("Number of Spawns: " .. Warfare.Settings.numSpawns)
            end
        end

    elseif action == "warfare_fight_keyboard" then
        Warfare.fight = not Warfare.fight

        if Warfare.fight then
            Warfare.Settings.Preview.active = false
        end


    elseif action == "warfare_shift_keyboard" then
        if activation == "press" then
            Warfare.Keys.shift = true
        else
            Warfare.Keys.shift = false
        end

    elseif action == "warfare_control_keyboard" then
        if activation == "press" then
            Warfare.Keys.control = true
        else
            Warfare.Keys.control = false
        end

    elseif action == "flight_toggle_keyboard" then
        Flight.active = not Flight.active

        if Flight.active then
            Flight.log("Flight On")
        else
            Flight.log("Flight Off")
        end

    elseif action == "flight_forward_keyboard" then
        if activation == "press" then
            Flight.Keys.forward = true
        else
            Flight.Keys.forward = false
        end

    elseif action == "flight_back_keyboard" then
        if activation == "press" then
            Flight.Keys.back = true
        else
            Flight.Keys.back = false
        end

    elseif action == "flight_left_keyboard" then
        if activation == "press" then
            Flight.Keys.left = true
        else
            Flight.Keys.left = false
        end

    elseif action == "flight_right_keyboard" then
        if activation == "press" then
            Flight.Keys.right = true
        else
            Flight.Keys.right = false
        end

    elseif action == "flight_up_keyboard" then
        if activation == "press" then
            Flight.Keys.up = true
        else
            Flight.Keys.up = false
        end

    elseif action == "flight_down_keyboard" then
        if activation == "press" then
            Flight.Keys.down = true
        else
            Flight.Keys.down = false
        end

    elseif action == "flight_fast_keyboard" then
        if activation == "press" then
            Flight.Keys.fast = true
        else
            Flight.Keys.fast = false
        end

    elseif action == "flight_slow_keyboard" then
        if activation == "press" then
            Flight.Keys.slow = true
        else
            Flight.Keys.slow = false
        end

    end
end


function Warfare:OnGameplayStarted()
    System.LogAlways(string.format("$5Loading Mod..."))

    Script.ReloadScript("scripts/warfare/utility.lua")
    Script.ReloadScript("scripts/warfare/commands.lua")
    Script.ReloadScript("scripts/warfare/simulation.lua")
    Script.ReloadScript("scripts/warfare/equipment.lua")
    Script.ReloadScript("scripts/warfare/spawn.lua")
    Script.ReloadScript("scripts/warfare/flight.lua")
    

    if ActionMapManager then
        ActionMapManager.EnableActionMap("warfare_action", true)
    else
        self.log("Failed to load keybinds")
    end

    System.ExecuteCommand("WH_AI_LOD_MaxCountDetail = 185")
    System.SetCVar("t_scale", 1)

    --player.Properties.WarfareProperties = {faction = 1}
    --table.insert(Warfare.SpawnedEntities, player)

    self.SpawnedEntities = {}
    self.PreviewEntities = {}
    self.fight = false

    local fac1 = {
        coat = "Kuttenberg"
    }
    local fac2 = {
        coat = "Semine"
    }

    table.insert(self.Factions, fac1)
    table.insert(self.Factions, fac2)

    self:setFactionTables(self.Factions[1])
    self:setFactionTables(self.Factions[2])

    --wh_cs_MaxAttackDistanceMoveOffset entity.soul:SetTarget(nil)
    --Warfare.log(player.soul:GetDerivedStat("fdm"))
    self.log("Ready")
end


UIAction.RegisterEventSystemListener(Warfare, "", "OnGameplayStarted", "OnGameplayStarted")
